package interviewprograms;

public class ChukkaluKanipisthayi1 
{
	public static void main(String[] args) throws Exception
	{
		//Same line(Horizontally)
		for(int i=1;i<=5;i++)
		{
			System.out.print("*");
		}
		
		System.out.println();
		
		//New line(Vertically)
		for(int i=1;i<=5;i++)
		{
			System.out.println("#");
		}
	}
}
